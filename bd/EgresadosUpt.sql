-- Tabla cargos
CREATE TABLE cargos (
    id INT PRIMARY KEY,
    nombre_cargo VARCHAR(100)
);

-- Tabla ubicaciones (solo país)
CREATE TABLE ubicaciones (
    id INT PRIMARY KEY,
    pais VARCHAR(100),
    ciudad VARCHAR(100)
);

-- Tabla tecnologias
CREATE TABLE tecnologias (
    id INT PRIMARY KEY,
    nombre_tecnologia VARCHAR(100)
);

-- Tabla egresados
CREATE TABLE egresados (
    id INT PRIMARY KEY,
    nombre VARCHAR(255),
    id_cargo INT,
    id_ubicacion INT,
    linkedin_url TEXT,
    anio_egreso INT,
    FOREIGN KEY (id_cargo) REFERENCES cargos(id),
    FOREIGN KEY (id_ubicacion) REFERENCES ubicaciones(id)
);

-- Tabla puente
CREATE TABLE egresado_tecnologia (
    id_egresado INT,
    id_tecnologia INT,
    FOREIGN KEY (id_egresado) REFERENCES egresados(id),
    FOREIGN KEY (id_tecnologia) REFERENCES tecnologias(id)
);

